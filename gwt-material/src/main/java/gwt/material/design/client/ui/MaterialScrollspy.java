public class MaterialScrollspy extends UnorderedList {

    public MaterialScrollspy() {
        super(CssName.SECTION, CssName.TABLE_OF_CONTENTS);
    }

    @Override
    protected void onLoad() {
        super.onLoad();

        scrollSpy();
    }

    public void scrollSpy() {
        $(".scrollspy").scrollSpy();
    }
}
